##### Class Names #####

# Convention: CapWords (CamelCase)
# Example: class BankAccount:
# Description: Each word starts with a capital letter.

##### Function and Variable Names #####

# Convention: lowercase_with_underscores
# Examples:
# Function: def calculate_tax():
# Variable: account_balance = 1000
# Description: All lowercase with underscores between words.

##### Constants #####

# Convention: ALL_CAPITALS_WITH_UNDERSCORES
# Example: PI = 3.14159
# Description: All uppercase letters with underscores.

##### Module Names #####

# Convention: lowercase (underscore_allowed)
# Example: import my_module
# Description: All lowercase, underscores for readability.

##### Method Names and Instance Variables #####

# Convention: lowercase_with_underscores
# Examples:
# Method: def get_balance(self):
# Instance Variable: self.account_id = 12345
# Description: Lowercase with underscores, like functions.

##### Special Names (Magic Methods) #####

# Convention: double_underscores
# Examples: __init__, __str__, __len__
# Description: Double underscores prefix and suffix, for special Python methods.

##### Private and "Protected" Member Names #####

# Convention: _single_leading_underscore
# Examples:
# Private Method: def _internal_update(self):
# Protected Variable: self._data = {}
# Description: Single underscore prefix for internal use.

##### Avoiding Name Clashes with Built-ins #####

# Convention: trailing_underscore_
# Examples: class_, list_
# Description: Trailing underscore to avoid conflicts with Python keywords.
